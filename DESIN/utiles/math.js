//CONSTANTES
Math.PI        // 3.14159...
Math.E         // 2.718...
Math.SQRT2     // √2
Math.LN2       // loge(2)
Math.LN10      // loge(10)

//FUNCIONES BÁSICAS
Math.abs(-5);       // 5 (valor absoluto)
Math.ceil(4.2);     // 5 (redondear hacia arriba)
Math.floor(4.9);    // 4 (redondear hacia abajo)
Math.round(4.5);    // 5 (redondeo normal)
Math.trunc(4.9);    // 4 (parte entera)

//POTENCIAS Y RAÍCES
Math.pow(2, 3);     // 8
Math.sqrt(16);      // 4
Math.cbrt(27);      // 3 (raíz cúbica)

//ALEATORIOS
Math.random();      // número entre 0 y 1
Math.floor(Math.random() * 10); // entero entre 0 y 9

//MÁXIMOS Y MÍNIMOS
Math.max(1, 5, 9);  // 9
Math.min(1, 5, 9);  // 1

//TRIGONOMETRÍA
Math.sin(Math.PI/2); // 1
Math.cos(0);         // 1
Math.tan(Math.PI/4); // 1

//LOGARITMOS Y EXPONENCIALES
Math.log(10);        // log natural
Math.log10(100);     // log base 10 → 2
Math.exp(1);         // e^1 → 2.718...

